void init();

function getText() {
  return I18N[state.lang];
}

async function init() {
  bindEvents();
  applyLanguage();
  setStatus(getText().loadingData);
  renderEmptyCard(getText().selectPrompt);

  try {
    const response = await fetch(DATA_URL, { cache: "no-store" });
    if (!response.ok) {
      throw new Error(`load-failed: ${response.status}`);
    }
    state.payload = await response.json();
    renderLevelFilter();
    renderPicker();
    const range = state.payload.meta.dateRange;
    setStatus(getText().loadedRange(state.payload.meta.totalExamFamilies, range.start, range.end));
  } catch (error) {
    console.error(error);
    setStatus(getText().loadFailed);
    renderEmptyCard(getText().loadFailed);
  }
}

function bindEvents() {
  dom.langToggleBtn.addEventListener("click", () => {
    state.lang = state.lang === "zh" ? "en" : "zh";
    applyLanguage();
    renderPicker();
    if (state.selectedIds.size > 0) {
      generatePersonalCalendar(false);
    } else if (state.payload) {
      const range = state.payload.meta.dateRange;
      setStatus(getText().loadedRange(state.payload.meta.totalExamFamilies, range.start, range.end));
      renderEmptyCard(getText().selectPrompt);
    }
  });

  dom.searchInput.addEventListener("input", (event) => {
    state.searchText = event.target.value.trim().toLowerCase();
    renderPicker();
  });

  dom.levelFilter.addEventListener("click", (event) => {
    const target = event.target;
    if (!(target instanceof HTMLElement)) {
      return;
    }
    const button = target.closest("button[data-level-filter]");
    if (!button) {
      return;
    }
    const next = button.dataset.levelFilter;
    if (!next || next === state.activeLevelFilter) {
      return;
    }
    state.activeLevelFilter = next;
    renderLevelFilter();
    renderPicker();
  });

  dom.selectVisibleBtn.addEventListener("click", () => {
    updateIdsSelection(Array.from(state.visibleIds), true);
  });

  dom.clearVisibleBtn.addEventListener("click", () => {
    updateIdsSelection(Array.from(state.visibleIds), false);
  });

  dom.generateBtn.addEventListener("click", () => {
    generatePersonalCalendar();
  });

  dom.alertToggle.addEventListener("change", () => {
    state.includeAlerts = dom.alertToggle.checked;
  });

  dom.exportPngBtn.addEventListener("click", () => {
    void exportAsPng();
  });

  dom.exportIcsBtn.addEventListener("click", () => {
    exportAsIcs();
  });

  dom.examPicker.addEventListener("change", (event) => {
    const target = event.target;
    if (!(target instanceof HTMLInputElement)) {
      return;
    }

    const subjectKey = target.dataset.subjectKey;
    if (subjectKey) {
      const ids = state.subjectIdMap.get(subjectKey) || [];
      updateIdsSelection(ids, target.checked);
      return;
    }

    const familyId = target.dataset.familyId;
    if (!familyId) {
      return;
    }

    if (target.checked) {
      state.selectedIds.add(familyId);
    } else {
      state.selectedIds.delete(familyId);
    }

    normalizeCieSelection();
    renderPicker();
    generatePersonalCalendar(false);
  });

  dom.examPicker.addEventListener("click", (event) => {
    const target = event.target;
    if (!(target instanceof HTMLElement)) {
      return;
    }
    const actionButton = target.closest("button[data-action]");
    if (!actionButton) {
      return;
    }
    const action = actionButton.dataset.action;
    if (action !== "select-level" && action !== "clear-level") {
      return;
    }
    const level = actionButton.dataset.level;
    if (!level) {
      return;
    }
    const ids = state.visibleByLevel.get(level) || [];
    updateIdsSelection(ids, action === "select-level");
  });
}

