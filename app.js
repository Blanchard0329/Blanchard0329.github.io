function renderLevelFilter() {
  const t = getText();
  const chips = dom.levelFilter.querySelectorAll("button[data-level-filter]");
  for (const chip of chips) {
    if (!(chip instanceof HTMLButtonElement)) {
      continue;
    }
    const key = chip.dataset.levelFilter || "ALL";
    chip.textContent = t.levelFilter[key] || key;
    const isActive = key === state.activeLevelFilter;
    chip.classList.toggle("is-active", isActive);
    chip.setAttribute("aria-selected", isActive ? "true" : "false");
  }
}

function renderPicker() {
  if (!state.payload) {
    return;
  }

  const t = getText();
  const families = state.payload.examFamilies
    .filter((family) => matchesSearch(family, state.searchText))
    .filter((family) => state.activeLevelFilter === "ALL" || family.level === state.activeLevelFilter);

  state.visibleIds = new Set(families.map((item) => item.id));
  state.visibleByLevel = new Map();
  state.subjectIdMap = new Map();

  if (!families.length) {
    dom.examPicker.innerHTML = `<div class="empty-state">${escapeHtml(t.noResults)}</div>`;
    refreshSelectionStatus();
    return;
  }

  const sections = [];
  for (const level of state.payload.meta.levels) {
    if (state.activeLevelFilter !== "ALL" && state.activeLevelFilter !== level) {
      continue;
    }

    const levelFamilies = families.filter((item) => item.level === level);
    if (!levelFamilies.length) {
      continue;
    }

    state.visibleByLevel.set(
      level,
      levelFamilies.map((item) => item.id)
    );

    const boardBlocks = BOARD_ORDER.map((board) => {
      const boardFamilies = levelFamilies.filter((item) => item.board === board);
      if (!boardFamilies.length) {
        return "";
      }

      const cards = board === "CIE" ? renderCieCards(level, boardFamilies) : renderEdexcelCards(boardFamilies);
      return `
        <section class="board-section board-${escapeHtml(board.toLowerCase())}">
          <div class="board-head">
            <h4 class="board-title">${escapeHtml(t.boardLabel[board] || board)}</h4>
            <span class="board-count">${escapeHtml(t.levelCount(boardFamilies.length))}</span>
          </div>
          <div class="card-grid">${cards.join("")}</div>
        </section>
      `;
    })
      .filter(Boolean)
      .join("");

    sections.push(`
      <section class="level-section level-${level}">
        <div class="level-head">
          <div>
            <h3 class="level-title">${escapeHtml(t.levelLabel[level] || level)}</h3>
            <div class="level-meta">${escapeHtml(t.levelCount(levelFamilies.length))}</div>
          </div>
          <div class="level-actions">
            <button class="level-btn" type="button" data-action="select-level" data-level="${level}">${escapeHtml(
      t.levelSelect
    )}</button>
            <button class="level-btn" type="button" data-action="clear-level" data-level="${level}">${escapeHtml(
      t.levelClear
    )}</button>
          </div>
        </div>
        ${boardBlocks}
      </section>
    `);
  }

  dom.examPicker.innerHTML = sections.join("");
  refreshSelectionStatus();
}

function renderEdexcelCards(families) {
  const t = getText();
  return [...families]
    .sort((left, right) => {
      const subjectDiff = left.subject.localeCompare(right.subject, "en", { sensitivity: "base" });
      if (subjectDiff !== 0) {
        return subjectDiff;
      }
      return left.code.localeCompare(right.code, "en", { sensitivity: "base" });
    })
    .map((family) => {
      const selected = state.selectedIds.has(family.id);
      const first = family.variants[0];
      const boardCode = `${t.boardShort[family.board] || family.board}: ${family.code}`;
      const subjectShort = buildIcsSubjectShort(family);
      const cardTitle = subjectShort ? `${family.subject} ${subjectShort}` : family.subject;
      const cardClass = selected ? "exam-card selected edx-card" : "exam-card edx-card";
      return `
        <label class="${cardClass}">
          <input class="card-check" type="checkbox" data-family-id="${escapeHtml(family.id)}" ${
            selected ? "checked" : ""
          } />
          <div class="card-top">
            <span class="card-code">${escapeHtml(boardCode)}</span>
          </div>
          <div class="card-title">${escapeHtml(cardTitle)}</div>
          <div class="card-sub">${escapeHtml(family.paper)}</div>
          <div class="card-date">${escapeHtml(formatDateLabel(first.date))}</div>
        </label>
      `;
    });
}

function renderCieCards(level, families) {
  const t = getText();
  const groups = groupFamiliesBySubject(families);
  return groups.map((group) => {
    const subjectKey = encodeURIComponent(`${level}|${group.board}|${group.subject}`);
    const groupIds = group.families.map((item) => item.id);
    state.subjectIdMap.set(subjectKey, groupIds);
    const selected = groupIds.every((id) => state.selectedIds.has(id));
    const boardCode = `${t.boardShort[group.board] || group.board}: ${getCieSyllabusCode(group.families)}`;
    const firstDate = getEarliestVariantDate(group.families);
    const summary = buildCiePaperSummary(group.families);
    const cardClass = selected ? "exam-card selected cie-card" : "exam-card cie-card";
    return `
      <label class="${cardClass}">
        <input class="card-check" type="checkbox" data-subject-key="${subjectKey}" ${selected ? "checked" : ""} />
        <div class="card-top">
          <span class="card-code">${escapeHtml(boardCode)}</span>
          <span class="card-tag">${escapeHtml(t.paperCount(group.families.length))}</span>
        </div>
        <div class="card-title">${escapeHtml(group.subject)}</div>
        <div class="card-sub">${escapeHtml(summary)}</div>
        <div class="card-date">${escapeHtml(formatDateLabel(firstDate))}</div>
      </label>
    `;
  });
}

function groupFamiliesBySubject(families) {
  const map = new Map();
  for (const family of families) {
    const groupSubject = buildCieSubjectGroupName(family);
    const key = `${family.board}|${groupSubject}`;
    if (!map.has(key)) {
      map.set(key, { board: family.board, subject: groupSubject, families: [] });
    }
    map.get(key).families.push(family);
  }

  return Array.from(map.values())
    .map((group) => {
      group.families.sort((left, right) => left.code.localeCompare(right.code, "en", { sensitivity: "base" }));
      return group;
    })
    .sort((left, right) => left.subject.localeCompare(right.subject, "en", { sensitivity: "base" }));
}

function buildCieSubjectGroupName(family) {
  const subject = family.subject;
  const paper = String(family.paper || "");
  if (/mathematics/i.test(subject)) {
    if (/\bcore\b/i.test(paper)) {
      return "Mathematics (Core)";
    }
    if (/\bextended\b/i.test(paper)) {
      return "Mathematics (Extended)";
    }
  }
  return subject;
}

function getCieSelectionKey(family) {
  return `${family.level}|${buildCieSubjectGroupName(family)}`;
}

function getCieSyllabusCode(families) {
  const codeSet = new Set(
    families.map((family) => {
      const parts = String(family.code).split("/");
      return parts[0] || family.code;
    })
  );
  return Array.from(codeSet).join("/");
}

function getEarliestVariantDate(families) {
  const dates = families
    .flatMap((family) => family.variants.map((variant) => variant.date))
    .sort((left, right) => left.localeCompare(right));
  return dates[0] || "";
}

function buildCiePaperSummary(families) {
  const t = getText();
  const types = [];
  for (const family of families) {
    const label = classifyCiePaperLabel(family.paper);
    if (!types.includes(label)) {
      types.push(label);
    }
  }
  const shown = types.slice(0, 4);
  if (types.length > 4) {
    shown.push(t.moreTypes(types.length - 4));
  }
  return t.ciePaperSummary(families.length, shown.join(t.paperJoiner));
}

function classifyCiePaperLabel(paper) {
  const text = paper.toLowerCase();
  const k = getText().paperType;

  if (text.includes("multiple choice")) {
    return k.multiple;
  }
  if (text.includes("alternative to practical") || text.includes("practical")) {
    return k.experiment;
  }
  if (text.includes("theory") || text.includes("structured questions") || text.includes("structured")) {
    return k.structure;
  }
  if (text.includes("reading and writing") || text.includes("reading, directed writing")) {
    return k.readingWriting;
  }
  if (text.includes("reading")) {
    return k.reading;
  }
  if (text.includes("writing")) {
    return k.writing;
  }
  if (text.includes("listening")) {
    return k.listening;
  }
  if (text.includes("speaking") || text.includes("oral")) {
    return k.speaking;
  }
  if (text.includes("poetry") || text.includes("prose") || text.includes("literature") || text.includes("drama")) {
    return k.literature;
  }
  if (
    text.includes("essay") ||
    text.includes("data response") ||
    text.includes("research methods") ||
    text.includes("issues and debates") ||
    text.includes("planning, analysis")
  ) {
    return k.essayData;
  }
  if (text.includes("coursework")) {
    return k.coursework;
  }
  if (text.includes("pure mathematics")) {
    return k.pureMath;
  }
  if (text.includes("mechanics")) {
    return k.mechanics;
  }
  if (text.includes("statistics") || text.includes("probability")) {
    return k.statistics;
  }

  return cleanupPaperLabel(paper);
}

function cleanupPaperLabel(paper) {
  return String(paper)
    .replace(/\s*\(?\d{2}\)?$/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function matchesSearch(family, searchText) {
  if (!searchText) {
    return true;
  }
  const haystack = [
    family.level,
    family.board,
    family.board === "Edexcel" ? "edx" : "",
    family.qualification,
    family.subject,
    family.code,
    family.paper,
    ...family.variants.map((variant) => `${variant.session} ${variant.date}`),
  ]
    .join(" ")
    .toLowerCase();
  return haystack.includes(searchText);
}

