function buildIcsContent(generated, includeAlerts) {
  const t = getText();
  const lines = [
    "BEGIN:VCALENDAR",
    "VERSION:2.0",
    "PRODID:-//Exam Calendar Generator//EN",
    "CALSCALE:GREGORIAN",
    "METHOD:PUBLISH",
    "X-WR-CALNAME:Personal Exam Calendar",
    "X-WR-TIMEZONE:Asia/Shanghai",
    "BEGIN:VTIMEZONE",
    "TZID:Asia/Shanghai",
    "BEGIN:STANDARD",
    "TZOFFSETFROM:+0800",
    "TZOFFSETTO:+0800",
    "TZNAME:CST",
    "DTSTART:19700101T000000",
    "END:STANDARD",
    "END:VTIMEZONE",
  ];

  const stamp = formatUtcStamp(new Date());
  for (const event of generated.examEvents) {
    const uid = `${event.familyId}-${event.date}-${event.session}@exam-calendar`;
    const subjectShort = buildIcsSubjectShort(event);
    const summary = subjectShort ? `${event.subject} ${subjectShort}` : `${event.subject} ${event.code}`;
    const description = [
      `Exam Code: ${event.code}`,
      `Subject: ${summary}`,
      `Full Name: ${event.paper}`,
      `Room: ${event.venue || "TBC"}`,
      event.arrival ? `Arrive: ${event.arrival}` : "",
      `Board: ${event.board}`,
      `Level: ${event.level}`,
      `Session: ${event.session}`,
      event.fullSupervisionRequired ? "Supervision: Full supervision required" : "",
      event.note ? `Note: ${event.note}` : "",
    ]
      .filter(Boolean)
      .join("\n");

    lines.push("BEGIN:VEVENT");
    lines.push(`UID:${escapeIcs(uid)}`);
    lines.push(`DTSTAMP:${stamp}`);
    lines.push(`DTSTART;TZID=Asia/Shanghai:${formatIcsDateTime(event.date, event.start)}`);
    lines.push(`DTEND;TZID=Asia/Shanghai:${formatIcsDateTime(event.date, event.end)}`);
    lines.push(`SUMMARY:${escapeIcs(summary)}`);
    if (event.venue) {
      lines.push(`LOCATION:${escapeIcs(event.venue)}`);
    }
    lines.push(`DESCRIPTION:${escapeIcs(description)}`);

    if (includeAlerts) {
      lines.push("BEGIN:VALARM");
      lines.push("ACTION:DISPLAY");
      lines.push("TRIGGER:-P1D");
      lines.push(`DESCRIPTION:${escapeIcs(t.reminderTomorrow(event.code))}`);
      lines.push("END:VALARM");

      lines.push("BEGIN:VALARM");
      lines.push("ACTION:DISPLAY");
      lines.push("TRIGGER:-PT2H");
      lines.push(`DESCRIPTION:${escapeIcs(t.reminderTwoHours(event.code))}`);
      lines.push("END:VALARM");
    }

    lines.push("END:VEVENT");
  }

  for (const event of generated.breakEvents) {
    const uid = `break-${event.date}-${event.start}-${event.end}@exam-calendar`;
    lines.push("BEGIN:VEVENT");
    lines.push(`UID:${escapeIcs(uid)}`);
    lines.push(`DTSTAMP:${stamp}`);
    lines.push(`DTSTART;TZID=Asia/Shanghai:${formatIcsDateTime(event.date, event.start)}`);
    lines.push(`DTEND;TZID=Asia/Shanghai:${formatIcsDateTime(event.date, event.end)}`);
    lines.push(`SUMMARY:${escapeIcs(`${t.breakTitle} (${event.durationMinutes} min)`)}`);
    lines.push(`DESCRIPTION:${escapeIcs(`${event.leftCode} -> ${event.rightCode}`)}`);
    lines.push("END:VEVENT");
  }

  lines.push("END:VCALENDAR");
  return foldIcsLines(lines).join("\r\n") + "\r\n";
}

function foldIcsLines(lines) {
  const output = [];
  for (const rawLine of lines) {
    if (rawLine.length <= 74) {
      output.push(rawLine);
      continue;
    }
    let line = rawLine;
    output.push(line.slice(0, 74));
    line = line.slice(74);
    while (line.length > 0) {
      output.push(` ${line.slice(0, 73)}`);
      line = line.slice(73);
    }
  }
  return output;
}

function escapeIcs(text) {
  return String(text)
    .replace(/\\/g, "\\\\")
    .replace(/\n/g, "\\n")
    .replace(/,/g, "\\,")
    .replace(/;/g, "\\;");
}

function formatIcsDateTime(date, time) {
  return `${date.replace(/-/g, "")}T${time.replace(":", "")}00`;
}

function formatUtcStamp(date) {
  return date.toISOString().replace(/[-:]/g, "").replace(/\.\d{3}/, "");
}

function minutesToHHMM(totalMinutes) {
  const safe = Math.max(0, totalMinutes);
  const hours = Math.floor(safe / 60);
  const minutes = safe % 60;
  return `${String(hours).padStart(2, "0")}:${String(minutes).padStart(2, "0")}`;
}

function downloadBlob(filename, blob) {
  const url = URL.createObjectURL(blob);
  const anchor = document.createElement("a");
  anchor.href = url;
  anchor.download = filename;
  document.body.append(anchor);
  anchor.click();
  anchor.remove();
  URL.revokeObjectURL(url);
}

function getSessionRank(session) {
  const match = session.match(/(\d+)/);
  return match ? Number(match[1]) : 1;
}

function getBoardShort(board) {
  return getText().boardShort[board] || board;
}

function formatImageVenueNote(note) {
  const matches = String(note || "").match(/\bG\d{1,2}\s*(?:\([^)]*\))?\s*:\s*[^;]+/gi) || [];
  return matches
    .map((item) =>
      item
        .replace(/\s*\([^)]*\)\s*(?=:)/g, "")
        .replace(/\s*:\s*/g, ": ")
        .replace(/\s+/g, " ")
        .trim()
    )
    .join("; ");
}

function buildIcsSubjectShort(event) {
  const code = String(event.code || "").toUpperCase();
  const paperName = String(event.paper || "");

  const cieMatch = code.match(/\/(\d+[A-Z]?)$/);
  if (cieMatch) {
    return cieMatch[1];
  }

  const paperPatterns = [
    { regex: /further pure mathematics\s*(\d+)/i, format: "FP" },
    { regex: /pure mathematics\s*(\d+)/i, format: "P" },
    { regex: /statistics?\s*(\d+)/i, format: "S" },
    { regex: /mechanics?\s*(\d+)/i, format: "M" },
    { regex: /decision mathematics?\s*(\d+)/i, format: "D" },
    { regex: /\bcomponent\s*(\d+)/i, format: "C" },
    { regex: /\bunit\s*(\d+)/i, format: "U" },
    { regex: /\bpaper\s*(\d+)/i, format: "P" },
  ];
  for (const item of paperPatterns) {
    const match = paperName.match(item.regex);
    if (match) {
      return `${item.format}${match[1]}`;
    }
  }

  const lastDigit = code.match(/(\d)$/)?.[1];
  if (!lastDigit) {
    return "";
  }
  if (code.startsWith("WFM")) {
    return `FP${lastDigit}`;
  }
  if (code.startsWith("WMA")) {
    return `P${lastDigit}`;
  }
  if (code.startsWith("WST")) {
    return `S${lastDigit}`;
  }
  if (code.startsWith("WME")) {
    return `M${lastDigit}`;
  }
  if (code.startsWith("WDM")) {
    return `D${lastDigit}`;
  }
  if (/^W[A-Z]{2}\d{2}$/.test(code)) {
    return `U${lastDigit}`;
  }
  return "";
}

function formatDateLabel(dateText) {
  const t = getText();
  const [year, month, day] = dateText.split("-").map((item) => Number(item));
  const date = new Date(year, month - 1, day);
  return `${dateText} ${t.weekdays[date.getDay()]}`;
}

